# –ü—Ä–æ–≤–µ—Ä–∫—É —ç–∫–≤–∏–≤–∞–ª–µ–Ω—Ç–Ω–æ—Å—Ç–∏ —á–µ—Ä–µ–∑ —Ç–∞–±–ª–∏—Ü—É –∏—Å—Ç–∏–Ω–Ω–æ—Å—Ç–∏

# –ü–æ—à–∞–≥–æ–≤—ã–π –ø—Ä–æ—Ü–µ—Å—Å –ø–æ—Å—Ç—Ä–æ–µ–Ω–∏—è –°–ö–ù–§

# –í–∏–∑—É–∞–ª–∏–∑–∞—Ü–∏—é –ø—Ä–æ—Ü–µ—Å—Å–∞ —Ä–µ–∑–æ–ª—é—Ü–∏–∏

# –û—Å–Ω–æ–≤–Ω–æ–π –≤—ã–≤–æ–¥: –æ–±–∞ –≤—ã—Ä–∞–∂–µ–Ω–∏—è –ª–æ–≥–∏—á–µ—Å–∫–∏ —ç–∫–≤–∏–≤–∞–ª–µ–Ω—Ç–Ω—ã, –∞ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç —á–µ—Ä–µ–∑ 
# —Å–∏—Å—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –ø—Ä–∏–º–µ–Ω–µ–Ω–∏–µ –ª–æ–≥–∏—á–µ—Å–∫–∏—Ö –∑–∞–∫–æ–Ω–æ–≤, –≤ –æ—Å–Ω–æ–≤–Ω–æ–º —á–µ—Ä–µ–∑ –ø–æ—Å—Ç—Ä–æ–µ–Ω–∏–µ —Å–æ–≤–µ—Ä—à–µ–Ω–Ω–æ–π –Ω–æ—Ä–º–∞–ª—å–Ω–æ–π —Ñ–æ—Ä–º—ã.




# —ç–∫–æ–Ω–æ–º–µ—Ç—Ä–∏–∫–∞

# –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –Ω—É–∂–Ω—ã–µ –±–∏–±–ª–∏–æ—Ç–µ–∫–∏
import numpy as np

# --- 1. –ò—Å—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ (–Ω–∞—à–∏ –Ω–∞–±–ª—é–¥–µ–Ω–∏—è) ---
# –î–æ–ø—É—Å—Ç–∏–º, —É –Ω–∞—Å –µ—Å—Ç—å 5 –Ω–∞–±–ª—é–¥–µ–Ω–∏–π
X = np.array([1, 2, 3, 4, 5])
Y = np.array([2, 3, 5, 4, 6])


# --- 2. –†–∞—Å—Å—á–∏—Ç–∞–µ–º —Å—Ä–µ–¥–Ω–∏–µ –∑–Ω–∞—á–µ–Ω–∏—è ---
mean_X = np.mean(X)
mean_Y = np.mean(Y)

print(f"–°—Ä–µ–¥–Ω–µ–µ XÃÑ = {mean_X:.2f}")
print(f"–°—Ä–µ–¥–Ω–µ–µ YÃÑ = {mean_Y:.2f}")

# --- 3. –í—ã–±–æ—Ä–æ—á–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è X ---
# Var(X) = Œ£(Xi - XÃÑ)¬≤ / (n - 1)
var_X = np.sum((X - mean_X)**2) / (len(X) - 1)
print(f"–í—ã–±–æ—Ä–æ—á–Ω–∞—è –¥–∏—Å–ø–µ—Ä—Å–∏—è Var(X) = {var_X:.2f}")



# --- 4. –í—ã–±–æ—Ä–æ—á–Ω–∞—è –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—è –º–µ–∂–¥—É X –∏ Y ---
# Cov(X,Y) = Œ£[(Xi - XÃÑ)(Yi - YÃÑ)] / (n - 1)
cov_XY = np.sum((X - mean_X)*(Y - mean_Y)) / (len(X) - 1)
print(f"–í—ã–±–æ—Ä–æ—á–Ω–∞—è –∫–æ–≤–∞—Ä–∏–∞—Ü–∏—è Cov(X,Y) = {cov_XY:.2f}")


# --- 5. –ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç—ã –ª–∏–Ω–µ–π–Ω–æ–π —Ä–µ–≥—Ä–µ—Å—Å–∏–∏ ---
b = cov_XY / var_X
a = mean_Y - b * mean_X

print(f"\n–ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç—ã –ª–∏–Ω–µ–π–Ω–æ–π —Ä–µ–≥—Ä–µ—Å—Å–∏–∏:")
print(f"b' (–Ω–∞–∫–ª–æ–Ω) = {b:.2f}")
print(f"a' (—Å–≤–æ–±–æ–¥–Ω—ã–π —á–ª–µ–Ω) = {a:.2f}")

# --- 6. –ü—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–∏–µ –ø–æ –º–æ–¥–µ–ª–∏ ---
# –ú–æ–¥–µ–ª—å: Y' = a' + b'X
Y_pred = a + b * X

print("\n–ü—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è Y':")
print(np.round(Y_pred, 2))


# --- 7. –î–ª—è –Ω–∞–≥–ª—è–¥–Ω–æ—Å—Ç–∏: —Å—Ä–∞–≤–Ω–µ–Ω–∏–µ —Ñ–∞–∫—Ç–∏—á–µ—Å–∫–∏—Ö –∏ –ø—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–Ω—ã—Ö ---
print("\n–°—Ä–∞–≤–Ω–µ–Ω–∏–µ —Ñ–∞–∫—Ç–∏—á–µ—Å–∫–∏—Ö –∏ –ø—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π:")
for xi, yi, ypi in zip(X, Y, Y_pred):
    print(f"X={xi},  Y={yi},  Y'={ypi:.2f}")
    
# --- 8. –ü—Ä–æ–≤–µ—Ä–∏–º –≤—Å—ë –≤—Å—Ç—Ä–æ–µ–Ω–Ω—ã–º–∏ —Ñ—É–Ω–∫—Ü–∏—è–º–∏ numpy ---
# numpy.cov –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–æ–≤–∞—Ä–∏–∞—Ü–∏–æ–Ω–Ω—É—é –º–∞—Ç—Ä–∏—Ü—É
cov_matrix = np.cov(X, Y, ddof=1)  # ddof=1 ‚Üí –≤—ã–±–æ—Ä–æ—á–Ω–∞—è
b_np = cov_matrix[0,1] / cov_matrix[0,0]
a_np = mean_Y - b_np * mean_X

print("\n–ü—Ä–æ–≤–µ—Ä–∫–∞ —Å NumPy:")
print(f"b' = {b_np:.2f}, a' = {a_np:.2f}")



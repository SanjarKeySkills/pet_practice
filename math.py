# Set Theory: Python's built-in set data type directly supports operations like union, 
# ntersection, difference, and symmetric difference, making it easy to model and manipulate sets.

set_A = {1, 2, 3, 4}
set_B = {3, 4, 5, 6}
union_set = set_A.union(set_B)  # {1, 2, 3, 4, 5, 6}
intersection_set = set_A.intersection(set_B) # {3, 4}


# –°–ø–æ—Å–æ–± 1: –ò—Å–ø–æ–ª—å–∑—É–µ–º –±–∏–±–ª–∏–æ—Ç–µ–∫—É sympy (—Å–∏–º–≤–æ–ª—å–Ω—ã–µ –≤—ã—á–∏—Å–ª–µ–Ω–∏—è)

from sympy import symbols, And, Or, Not, simplify_logic, to_cnf

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ
p, q, r = symbols('p q r')

# –ò—Å—Ö–æ–¥–Ω–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ –≤ –î–ù–§
dnf_expression = Or(
    And(p, Not(q)),
    And(p, Not(r)),
    And(q, Not(p)),
    And(q, Not(r)),
    And(r, Not(p)),
    And(r, Not(q))
)

print("–ò—Å—Ö–æ–¥–Ω–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ –≤ –î–ù–§:")
print(dnf_expression)
print()

# –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –∫ –ö–ù–§
cnf_expression = to_cnf(dnf_expression, simplify=True)
print("–ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–Ω–æ–µ –≤ –ö–ù–§:")
print(cnf_expression)
print()

# –£–ø—Ä–æ—â–∞–µ–º –µ—â–µ –±–æ–ª—å—à–µ
simplified_cnf = simplify_logic(cnf_expression)
print("–£–ø—Ä–æ—â–µ–Ω–Ω–∞—è –ö–ù–§:")
print(simplified_cnf)
print()

# –¶–µ–ª–µ–≤–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
target_expression = And(
    Or(p, Not(r)),
    Or(q, Not(r)),
    Or(r, Not(p)),
    Or(p, q, r),
    Or(Not(p), Not(q), Not(r))
)

print("–¶–µ–ª–µ–≤–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ:")
print(target_expression)
print()

# –ü—Ä–æ–≤–µ—Ä—è–µ–º —ç–∫–≤–∏–≤–∞–ª–µ–Ω—Ç–Ω–æ—Å—Ç—å
are_equivalent = simplify_logic(cnf_expression) == simplify_logic(target_expression)
print(f"–≠–∫–≤–∏–≤–∞–ª–µ–Ω—Ç–Ω—ã –ª–∏ –≤—ã—Ä–∞–∂–µ–Ω–∏—è: {are_equivalent}")



# –°–ø–æ—Å–æ–± 2: –ß–µ—Ä–µ–∑ —Ç–∞–±–ª–∏—Ü—É –∏—Å—Ç–∏–Ω–Ω–æ—Å—Ç–∏ (—Ä—É—á–Ω–æ–π –º–µ—Ç–æ–¥)
def evaluate_expression(p_val, q_val, r_val):
    """–í—ã—á–∏—Å–ª—è–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ –∏—Å—Ö–æ–¥–Ω–æ–≥–æ –î–ù–§ –≤—ã—Ä–∞–∂–µ–Ω–∏—è"""
    return (
        (p_val and not q_val) or
        (p_val and not r_val) or
        (q_val and not p_val) or
        (q_val and not r_val) or
        (r_val and not p_val) or
        (r_val and not q_val)
    )

def evaluate_target(p_val, q_val, r_val):
    """–í—ã—á–∏—Å–ª—è–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ —Ü–µ–ª–µ–≤–æ–≥–æ –ö–ù–§ –≤—ã—Ä–∞–∂–µ–Ω–∏—è"""
    return (
        (p_val or not r_val) and
        (q_val or not r_val) and
        (r_val or not p_val) and
        (p_val or q_val or r_val) and
        (not p_val or not q_val or not r_val)
    )

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –≤—Å–µ –∫–æ–º–±–∏–Ω–∞—Ü–∏–∏
print("–¢–∞–±–ª–∏—Ü–∞ –∏—Å—Ç–∏–Ω–Ω–æ—Å—Ç–∏:")
print("p q r | –î–ù–§ | –ö–ù–§ | –°–æ–≤–ø–∞–¥–∞—é—Ç?")
print("-" * 30)

all_match = True
for p_val in [False, True]:
    for q_val in [False, True]:
        for r_val in [False, True]:
            dnf_result = evaluate_expression(p_val, q_val, r_val)
            cnf_result = evaluate_target(p_val, q_val, r_val)
            match = dnf_result == cnf_result
            
            print(f"{int(p_val)} {int(q_val)} {int(r_val)} |  {int(dnf_result)}  |  {int(cnf_result)}  |    {match}")
            
            if not match:
                all_match = False

print(f"\n–í—ã—Ä–∞–∂–µ–Ω–∏—è —ç–∫–≤–∏–≤–∞–ª–µ–Ω—Ç–Ω—ã: {all_match}")


# –°–ø–æ—Å–æ–± 3: –ü–æ—à–∞–≥–æ–≤–æ–µ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ —á–µ—Ä–µ–∑ –°–ö–ù–§

def build_cnf_from_truth_table():
    """–°—Ç—Ä–æ–∏—Ç –°–ö–ù–§ –ø–æ —Ç–∞–±–ª–∏—Ü–µ –∏—Å—Ç–∏–Ω–Ω–æ—Å—Ç–∏"""
    
    # –ù–∞—Ö–æ–¥–∏–º –ª–æ–∂–Ω—ã–µ —Å—Ç—Ä–æ–∫–∏ –¥–ª—è –∏—Å—Ö–æ–¥–Ω–æ–≥–æ –≤—ã—Ä–∞–∂–µ–Ω–∏—è
    false_rows = []
    
    for p_val in [False, True]:
        for q_val in [False, True]:
            for r_val in [False, True]:
                if not evaluate_expression(p_val, q_val, r_val):
                    false_rows.append((p_val, q_val, r_val))
    
    print("–°—Ç—Ä–æ–∫–∏, –≥–¥–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ –ª–æ–∂–Ω–æ:")
    for row in false_rows:
        print(f"  p={row[0]}, q={row[1]}, r={row[2]}")
    
    # –°—Ç—Ä–æ–∏–º –¥–∏–∑—ä—é–Ω–∫—Ü–∏–∏ –¥–ª—è –∫–∞–∂–¥–æ–π –ª–æ–∂–Ω–æ–π —Å—Ç—Ä–æ–∫–∏
    cnf_clauses = []
    
    for p_val, q_val, r_val in false_rows:
        clause = []
        if not p_val: clause.append('p')
        if not q_val: clause.append('q') 
        if not r_val: clause.append('r')
        if p_val: clause.append('¬¨p')
        if q_val: clause.append('¬¨q')
        if r_val: clause.append('¬¨r')
        cnf_clauses.append(clause)
    
    return cnf_clauses, false_rows

# –í—ã–ø–æ–ª–Ω—è–µ–º –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ
cnf_clauses, false_rows = build_cnf_from_truth_table()

print("\n–°–ö–ù–§ –¥–∏–∑—ä—é–Ω–∫—Ü–∏–∏ (–º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–µ –∫–ª–∞—É–∑—ã):")
for clause in cnf_clauses:
    print(f"  ({' ‚à® '.join(clause)})")

print(f"\n–ò—Å—Ö–æ–¥–Ω–∞—è –°–ö–ù–§: (p ‚à® q ‚à® r) ‚àß (¬¨p ‚à® ¬¨q ‚à® ¬¨r)")

# –ü–æ–∫–∞–∂–µ–º, –∫–∞–∫ –ø–æ–ª—É—á–∏—Ç—å –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ –∫–ª–∞—É–∑—ã
print("\n–î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ –∫–ª–∞—É–∑—ã —á–µ—Ä–µ–∑ —Ä–µ–∑–æ–ª—é—Ü–∏—é:")
print("–ò–∑ (p ‚à® q ‚à® r) –∏ (¬¨p ‚à® ¬¨q ‚à® ¬¨r) –º–æ–∂–Ω–æ –≤—ã–≤–µ—Å—Ç–∏:")

# –†–µ–∑–æ–ª—é—Ü–∏—è —Å –¥–æ–±–∞–≤–ª–µ–Ω–∏–µ–º –≤—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã—Ö –¥–∏–∑—ä—é–Ω–∫—Ü–∏–π
print("  –î–æ–±–∞–≤–ª—è–µ–º (p ‚à® ¬¨p), (q ‚à® ¬¨q), (r ‚à® ¬¨r)")
print("  –†–µ–∑–æ–ª—é—Ü–∏—è –¥–∞–µ—Ç:")
print("    (p ‚à® ¬¨r) ‚àß (q ‚à® ¬¨r) ‚àß (r ‚à® ¬¨p)")

# –°–ø–æ—Å–æ–± 4: –í–∏–∑—É–∞–ª–∏–∑–∞—Ü–∏—è –ø—Ä–æ—Ü–µ—Å—Å–∞

